#include <iostream>
#include <cstdlib>
using namespace std;
int main(){
    // Изменение кодировки консоли: 
    system("chcp 1251>nul");
    // Верхняя граница суммы, значение суммы и индексная переменная:
    int n=10, s=0, k=1;
    // Оператор цикла для вычесления сумма:
    while(k<=n){
        // Добавление нового слагаемого к сумме:
        s=s+k*k;
        // Увличение (на 1) значения индексной переменной:
        k++;
    }
    // Отображение результата вычислений:
    cout<<"Сумма квадратов от 1 до "<<n<<": "<<s<<endl;
    // Задержка консольного окна:
    system("pause>nul");
    return 0;
}
